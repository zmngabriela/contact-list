class ContactClass {
  name: string
  email: string
  phone: string
  avatar: string
  description: string

  constructor(name: string, email: string, phone: string, avatar: string, description: string) {
    this.name = name,
    this.email = email,
    this.phone = phone,
    this.avatar = avatar,
    this.description = description
  }
}

export default ContactClass
